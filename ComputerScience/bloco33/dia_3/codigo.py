def is_odd(number):
    "Retorna True se um número é ímpar, senão False."
    return number % 2 != 0


def is_even(number):
    "Retorna True se um numero é par, senão False."
    return number % 2 == 0


def divide(a_number, other_number):
    "Retorna a divisão de a_number por other_number"
    return a_number / other_number
